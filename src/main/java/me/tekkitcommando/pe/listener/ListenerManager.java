package me.tekkitcommando.pe.listener;

import me.tekkitcommando.pe.PromotionEssentials;

public class ListenerManager {

    private static final PromotionEssentials plugin = PromotionEssentials.getInstance();

    public static void setupListeners() {
        plugin.getServer().getPluginManager().registerEvents(new PlayerChatListener(plugin), plugin);
        plugin.getServer().getPluginManager().registerEvents(new PlayerJoinListener(plugin), plugin);
        plugin.getServer().getPluginManager().registerEvents(new PlayerMoveListener(plugin), plugin);
        plugin.getServer().getPluginManager().registerEvents(new PlayerInteractListener(plugin), plugin);
        plugin.getServer().getPluginManager().registerEvents(new SignChangeListener(plugin), plugin);
        plugin.getServer().getPluginManager().registerEvents(new PlayerKillListener(plugin), plugin);
        plugin.getServer().getPluginManager().registerEvents(new PlayerLeaveListener(plugin), plugin);
    }
}
